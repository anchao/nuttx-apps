uint32_t lv_tick_interface(void)
{
  struct timespec ts;

#ifdef CONFIG_CLOCK_MONOTONIC
  clock_gettime(CLOCK_MONOTONIC, &ts);
#else
  clock_gettime(CLOCK_REALTIME, &ts);
#endif

  return ts.tv_sec * 1000 + ts.tv_nsec / 1000000;
}
